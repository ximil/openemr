namespace Tester\Model;
use Zend\Db\TableGateway\AbstractTableGateway;
use Zend\Db\Adapter\Adapter;
use Zend\Db\ResultSet\ResultSet;
use Zend\Db\Sql\Select;
use Zend\Db\Sql\Sql;
use Zend\Db\Sql\Where;
use \Application\Model\ApplicationTable ;

class TesterTable extends AbstractTableGateway
{
    protected $table = 'tester';

    public function __construct(Adapter $adapter)
    {
        $this->adapter = $adapter;
        $this->resultSetPrototype = new ResultSet();
        $this->resultSetPrototype->setArrayObjectPrototype(new Tester());
        $this->initialize();
    }
    
    public function acl_check($user_id,$section_id){
        $obj    = new ApplicationTable;
        return $obj->aclcheck($user_id,$section_id);
    }
}
